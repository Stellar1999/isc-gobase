package isc

import (
	"fmt"
	"io/ioutil"
)

func DefaultBanner() {
	fmt.Printf("%s\n", defaultBanner)
}

func Banner(filePath string) {
	if b, err := ioutil.ReadFile(filePath); err == nil {
		str := string(b)
		fmt.Printf("%s\n", str)
	} else {
		fmt.Printf("%s\n", defaultBanner)
	}
}

